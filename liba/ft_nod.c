#include "libft.h"

int			ft_nod(char *s1, char *s2)
{
	int		p1;
	int		p2;
	int		i;
	int		k;

	if (!s1 || !s2)
		return (0);
	p1 = ft_atoi(s1);
	p2 = ft_atoi(s2);
	i = 1;
	k = 0;
	while (i <= p1 || i <= p2)
	{
		if ((p1 % i == 0) && (p2 % i == 0))
			k = i;
		i++;
	}
	return (k);
}
