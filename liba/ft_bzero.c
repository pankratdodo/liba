#include "libft.h"

void				ft_bzero(void *s, size_t n)
{
	unsigned char	*k;
	size_t			i;

	k = (unsigned char *)s;
	i = 0;
	while (n)
	{
		k[i] = 0;
		n--;
		i++;
	}
}
