#include "libft.h"

char	*ft_strrev(char *str)
{
	int i;

	i = 0;
	if (!str)
		return (0);
	while (str[i])
		i++;
	while (i >= 0)
	{
		ft_putchar(str[i]);
		i--;
	}
	return (0);
}
