#include "libft.h"

void		ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*el;

	el = lst;
	if (lst && f)
	{
		while (el)
		{
			f(el);
			el = el->next;
		}
	}
}
